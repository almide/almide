use std::fs;
use std::path::Path;

/// Read a runtime source file, INLINING any `include!("X.rs")` directive with the
/// referenced file's content (resolved relative to this file's directory), recursively.
/// The runtime is embedded as ONE source string per module and assembled by
/// concatenation — it does not ship the sibling part files — so an `include!` (used to
/// keep each on-disk file under the 1000-line limit) must be resolved at embed time, or
/// it dangles in the generated crate (`couldn't read X.rs`).
fn read_with_includes(path: &Path) -> String {
    let content = fs::read_to_string(path).unwrap();
    let dir = path.parent().unwrap();
    let mut out = String::new();
    for line in content.lines() {
        let t = line.trim();
        let inc_name = t
            .strip_prefix("include!(\"")
            .and_then(|r| r.strip_suffix("\");").or_else(|| r.strip_suffix("\")")));
        if let Some(fname) = inc_name {
            out.push_str(&read_with_includes(&dir.join(fname)));
            out.push('\n');
        } else {
            out.push_str(line);
            out.push('\n');
        }
    }
    out
}

/// File names that are `include!`d by another file (the split "part" files). They are
/// inlined into their parent's embedded source by [`read_with_includes`], so they must
/// NOT also be emitted as their own top-level runtime module entry.
fn included_part_names(entries: &[fs::DirEntry]) -> std::collections::HashSet<String> {
    let mut parts = std::collections::HashSet::new();
    for e in entries {
        let content = fs::read_to_string(e.path()).unwrap();
        for line in content.lines() {
            let t = line.trim();
            if let Some(fname) = t
                .strip_prefix("include!(\"")
                .and_then(|r| r.strip_suffix("\");").or_else(|| r.strip_suffix("\")")))
            {
                parts.insert(fname.to_string());
            }
        }
    }
    parts
}

pub fn generate(workspace_root: &Path, out_dir: &Path) {
    let rust_dir = workspace_root.join("runtime/rs/src");
    if !rust_dir.exists() {
        return;
    }

    let mut rust_entries: Vec<_> = fs::read_dir(&rust_dir)
        .unwrap()
        .filter_map(|e| e.ok())
        .filter(|e| e.path().extension().map_or(false, |ext| ext == "rs"))
        .filter(|e| e.file_name() != "lib.rs")
        .collect();
    rust_entries.sort_by_key(|e| e.file_name());
    // Drop the split "part" files — their content is inlined into their parent module's
    // embedded string (see read_with_includes), so they are not standalone modules.
    let parts = included_part_names(&rust_entries);
    rust_entries.retain(|e| !parts.contains(e.file_name().to_str().unwrap()));

    let mut rust_out = String::new();
    rust_out.push_str("// AUTO-GENERATED by build.rs from runtime/rs/src/ — DO NOT EDIT\n\n");
    rust_out.push_str("pub const RUST_RUNTIME_MODULES: &[(&str, &str)] = &[\n");
    for entry in &rust_entries {
        let stem = entry
            .path()
            .file_stem()
            .unwrap()
            .to_str()
            .unwrap()
            .to_string();
        let content = read_with_includes(&entry.path());
        rust_out.push_str(&format!("    (\"{stem}\", {content:?}),\n"));
    }
    rust_out.push_str("];\n\n");

    // Auto-extract inter-module dependencies by scanning each module's
    // source for `almide_rt_{other}_` references. No manual whitelist.
    let module_names: Vec<String> = rust_entries.iter().map(|e| {
        e.path().file_stem().unwrap().to_str().unwrap().to_string()
    }).collect();

    rust_out.push_str("/// Inter-module runtime dependencies, auto-extracted from source.\n");
    rust_out.push_str("/// If module A's source references `almide_rt_B_*`, A depends on B.\n");
    rust_out.push_str("pub const RUNTIME_DEPS: &[(&str, &[&str])] = &[\n");
    for entry in &rust_entries {
        let stem = entry.path().file_stem().unwrap().to_str().unwrap().to_string();
        let content = read_with_includes(&entry.path());
        let mut deps: Vec<&str> = Vec::new();
        for other in &module_names {
            if *other == stem { continue; }
            let prefix = format!("almide_rt_{}_", other);
            if content.contains(&prefix) {
                deps.push(other);
            }
        }
        if !deps.is_empty() {
            rust_out.push_str(&format!("    (\"{}\", &[{}]),\n", stem,
                deps.iter().map(|d| format!("\"{}\"", d)).collect::<Vec<_>>().join(", ")));
        }
    }
    rust_out.push_str("];\n");

    // ── Embed the almide-kernel crate as an inlinable `mod almide_kernel` ──
    // runtime/rs/src/matrix.rs calls `almide_kernel::…` for the SIMD hot paths, but
    // the `almide test`/`almide build` fast paths compile the runtime as a single
    // crate (rlib or inlined) with no `almide_kernel` extern — so matrix specs failed
    // to resolve the crate. Embed the kernel source so emit_runtime_crate / emit_source
    // can drop it in as a crate-local module wherever `matrix` is used; no extern, no
    // rlib plumbing, and the cargo path gets it for free too.
    //
    // Kernel modules reference siblings via `crate::silu` etc.; wrapped one level deep
    // under `mod almide_kernel`, that sibling path is `super::silu`, so rewrite
    // `crate::` → `super::`. Test blocks are stripped — the runtime is built with
    // `--test`, so the kernel's own `#[cfg(test)]` would otherwise run inside every spec.
    let kernel_dir = workspace_root.join("crates/almide-kernel/src");
    let kernel_inline = if kernel_dir.exists() {
        let lib = fs::read_to_string(kernel_dir.join("lib.rs")).unwrap_or_default();
        let mut mods: Vec<String> = lib
            .lines()
            .filter_map(|l| l.trim().strip_prefix("pub mod ").and_then(|s| s.strip_suffix(';')))
            .map(|s| s.trim().to_string())
            .collect();
        mods.sort();
        let mut inline = String::new();
        inline.push_str("#[allow(dead_code, unused_imports, unused_variables, unused_parens, unused_mut, non_snake_case, clippy::all)]\nmod almide_kernel {\n");
        for m in &mods {
            // `bridge` is the kernel's STANDALONE bench/ABI-glue module, written against
            // the pre-0.28 nested `Vec<Vec<f64>>` matrix; nothing in the runtime calls
            // `bridge::` (matrix.rs calls the kernels directly on the flat struct).
            // Embedding it shipped a second, stale `AlmideMatrix` into every generated
            // matrix crate — and its alias line was the exact marker the old burn
            // splicer keyed on, so the splicer fired INSIDE the bridge and produced
            // invalid Rust (#739). Kernel compute modules only.
            if m == "bridge" {
                continue;
            }
            if let Ok(src) = fs::read_to_string(kernel_dir.join(format!("{m}.rs"))) {
                let body = strip_test_blocks(&src).replace("crate::", "super::");
                inline.push_str(&format!("pub mod {m} {{\n{body}\n}}\n"));
            }
        }
        inline.push_str("}\n");
        println!("cargo:rerun-if-changed={}", kernel_dir.display());
        inline
    } else {
        String::new()
    };
    rust_out.push_str("\n/// almide-kernel embedded as an inlinable `mod almide_kernel`, dropped into the\n");
    rust_out.push_str("/// runtime source wherever the `matrix` module is used (see emit_runtime_crate).\n");
    rust_out.push_str(&format!("pub const ALMIDE_KERNEL_INLINE: &str = {kernel_inline:?};\n"));

    fs::write(out_dir.join("rust_runtime.rs"), &rust_out).unwrap();

    // Auto-extract the runtime helpers whose LAST positional parameter is a generic
    // `F: Fn(...)` element fn (the value codec combinators — encode/decode list,
    // option, …). Their fn argument must stay a raw `impl Fn`, NOT the uniform
    // `Rc<dyn Fn>` closure value. Derived from the signatures themselves (no
    // hand-maintained allow-list) so a new codec combinator needs no codegen edit.
    let mut raw_fn_helpers: Vec<String> = Vec::new();
    for entry in &rust_entries {
        let content = read_with_includes(&entry.path());
        raw_fn_helpers.extend(extract_raw_fn_last_arg_helpers(&content));
    }
    raw_fn_helpers.sort();
    raw_fn_helpers.dedup();

    let mut modes_out = String::new();
    modes_out.push_str("// AUTO-GENERATED by build.rs from runtime/rs/src/ — DO NOT EDIT\n\n");
    modes_out.push_str("/// Runtime helpers whose LAST positional argument is a generic `F: Fn(...)`\n");
    modes_out.push_str("/// element fn (value codec combinators) — extracted from the signatures.\n");
    modes_out.push_str("pub fn takes_raw_fn_last_arg(name: &str) -> bool {\n");
    if raw_fn_helpers.is_empty() {
        modes_out.push_str("    let _ = name;\n    false\n");
    } else {
        modes_out.push_str("    matches!(name,\n");
        for (i, h) in raw_fn_helpers.iter().enumerate() {
            let sep = if i + 1 < raw_fn_helpers.len() { " |" } else { "" };
            modes_out.push_str(&format!("        \"{}\"{}\n", h, sep));
        }
        modes_out.push_str("    )\n");
    }
    modes_out.push_str("}\n");
    fs::write(out_dir.join("runtime_fn_modes.rs"), &modes_out).unwrap();

    println!("cargo:rerun-if-changed={}", rust_dir.display());
}

/// Names of `pub fn`s whose declared generics include a `<G>: Fn(...)` bound and
/// whose LAST positional parameter has type exactly that generic `G`. These take a
/// raw `impl Fn` (monomorphized), not a `Rc<dyn Fn>` closure value.
fn extract_raw_fn_last_arg_helpers(content: &str) -> Vec<String> {
    let mut out = Vec::new();
    let mut cursor = 0;
    while let Some(rel) = content[cursor..].find("pub fn ") {
        let (new_cursor, matched) = try_raw_fn_last_arg_helper_at(content, cursor, rel);
        cursor = new_cursor;
        if let Some(name) = matched {
            out.push(name);
        }
    }
    out
}

/// One `while` iteration of `extract_raw_fn_last_arg_helpers`, extracted
/// (cog>30 decomposition). The original's mid-loop `continue`s only ever
/// skipped pushing to `out` for THIS `pub fn` — `cursor` was already
/// advanced past its name before any of them — so this helper returns
/// `(new_cursor, Option<matched_name>)` instead: same "advance, maybe
/// record" shape, not an early-exit-the-whole-function hazard.
fn try_raw_fn_last_arg_helper_at(content: &str, cursor: usize, rel: usize) -> (usize, Option<String>) {
    let name_start = cursor + rel + "pub fn ".len();
    let name_end = content[name_start..]
        .find(|c: char| !(c.is_alphanumeric() || c == '_'))
        .map(|i| name_start + i)
        .unwrap_or(content.len());
    let name = content[name_start..name_end].to_string();
    let new_cursor = name_end;

    let rest = content[name_end..].trim_start();
    // Optional `<generics>` then `(params)`.
    let (generics, after) = if rest.starts_with('<') {
        match balanced(rest, '<', '>') {
            Some(end) => (&rest[1..end], rest[end + 1..].trim_start()),
            None => return (new_cursor, None),
        }
    } else {
        ("", rest)
    };
    if !after.starts_with('(') {
        return (new_cursor, None);
    }
    let params = match balanced(after, '(', ')') {
        Some(end) => &after[1..end],
        None => return (new_cursor, None),
    };

    // Generics with a `Fn` bound: `F: Fn(...) -> _`.
    let fn_generics: Vec<&str> = split_top_level(generics)
        .into_iter()
        .filter_map(|g| {
            let g = g.trim();
            let colon = g.find(':')?;
            if g[colon + 1..].trim_start().starts_with("Fn") {
                Some(g[..colon].trim())
            } else {
                None
            }
        })
        .collect();
    if fn_generics.is_empty() {
        return (new_cursor, None);
    }

    // Last param's type is exactly one of those Fn generics.
    if let Some(last) = split_top_level(params).last() {
        if let Some(colon) = last.rfind(':') {
            let ty = last[colon + 1..].trim();
            if fn_generics.contains(&ty) {
                return (new_cursor, Some(name));
            }
        }
    }
    (new_cursor, None)
}

/// Index of the close delimiter matching the open at `s[0]` (`s` must start with
/// `open`), honoring nesting. The `>` in a `->` return arrow is NOT a closing angle
/// bracket (e.g. `<F: Fn(T) -> Value>`). Returns None if unbalanced.
fn balanced(s: &str, open: char, close: char) -> Option<usize> {
    let mut depth = 0i32;
    let mut prev = ' ';
    for (i, c) in s.char_indices() {
        if c == close && close == '>' && prev == '-' {
            prev = c;
            continue;
        }
        if c == open {
            depth += 1;
        } else if c == close {
            depth -= 1;
            if depth == 0 {
                return Some(i);
            }
        }
        prev = c;
    }
    None
}

/// Split by top-level commas, ignoring commas nested in `<>`, `()`, or `[]`. The
/// `>` in a `->` return arrow does not close an angle bracket.
fn split_top_level(s: &str) -> Vec<&str> {
    let mut parts = Vec::new();
    let mut depth = 0i32;
    let mut start = 0;
    let mut prev = ' ';
    for (i, c) in s.char_indices() {
        match c {
            '<' | '(' | '[' => depth += 1,
            '>' if prev == '-' => {}
            '>' | ')' | ']' => depth -= 1,
            ',' if depth == 0 => {
                parts.push(&s[start..i]);
                start = i + 1;
            }
            _ => {}
        }
        prev = c;
    }
    if !s[start..].trim().is_empty() {
        parts.push(&s[start..]);
    }
    parts
}

/// Strip `#[cfg(test)] mod tests { … }` blocks. Mirror of the codegen-side stripper:
/// line-based brace counting (per-line string braces like `"i={i}"` net to zero, so
/// they don't desync the depth). Keeps the embedded kernel free of test code, which
/// would otherwise run under the runtime's `--test` build inside every spec.
fn strip_test_blocks(src: &str) -> String {
    let mut out = String::new();
    let mut depth = 0i32;
    let mut in_test_mod = false;
    for line in src.lines() {
        let trimmed = line.trim();
        if !in_test_mod && (trimmed.starts_with("#[cfg(test)]") || trimmed.starts_with("mod tests")) {
            in_test_mod = true;
            depth = 0;
        }
        if in_test_mod {
            for ch in line.chars() {
                if ch == '{' { depth += 1; }
                if ch == '}' { depth -= 1; }
            }
            if depth <= 0 && line.contains('}') {
                in_test_mod = false;
            }
            continue;
        }
        out.push_str(line);
        out.push('\n');
    }
    out
}
